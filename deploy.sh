#!/bin/bash

# 🌟 ตั้งค่าเซิร์ฟเวอร์ของคุณตรงนี้ 🌟
VPS_IP="45.32.122.181"  # <--- เปลี่ยนเป็น IP ของ VPS คุณ
VPS_USER="root"       # <--- ใช้ root หรือ user ที่มีสิทธิ์เขียนไฟล์

# รับค่าโดเมนจากคำสั่ง
DOMAIN=$1

if [ -z "$DOMAIN" ]; then
    echo "❌ ลืมใส่ชื่อโดเมนลูกพี่! วิธีใช้: ./deploy.sh slotza555.com"
    exit 1
fi

echo "🚀 [1/2] กำลัง Build โปรเจกต์ Astro ให้เป็น Static Files..."
# รันคำสั่ง Build ของ Astro
npx astro build --force

# เช็คว่า Build ผ่านไหม ถ้าพังให้หยุดทำงาน
if [ $? -ne 0 ]; then
    echo "❌ โค้ดมีปัญหา Build ไม่ผ่าน! ไปแก้โค้ดก่อนนะ"
    exit 1
fi

echo "⚡ [2/2] โค้ดพร้อม! กำลังยิงไฟล์ขึ้น VPS ไปที่โดเมน $DOMAIN ..."
# พระเอกของเรา! ใช้ rsync ยิงโฟลเดอร์ dist/ ไปที่ VPS
# -a = เอาไปให้หมดทั้งโฟลเดอร์
# -v = โชว์ให้ดูด้วยว่าส่งไฟล์อะไรไปบ้าง
# -z = บีบอัดไฟล์ตอนส่ง (ประหยัดเน็ต โคตรไว)
# --delete = ถ้าลบไฟล์ในคอม ใน VPS ก็จะถูกลบตาม (ทำให้เซิร์ฟเวอร์คลีนตลอดเวลา)
rsync -avz --delete dist/ $VPS_USER@$VPS_IP:/var/www/$DOMAIN/dist/

echo "🎉 เรียบร้อย! เว็บ $DOMAIN ออนไลน์แล้ว! เร็วปานสายฟ้าแลบ ⚡"